my_tuple = ('a','p','p','l','e',)
 
# Toán tử in
print('a' in my_tuple)
# Output: True
if 'a' in my_tuple: print('a in my_tuple')
else: print('a not in my_tuple')
print('b' in my_tuple)
# Output: False
if 'b' in my_tuple: print('b in my_tuple')
else: print('b not in my_tuple')
# Toán tử not in
print('g' not in my_tuple)
# Output: True
if 'g' in my_tuple: print('g in my_tuple')
else: print('g not in my_tuple')